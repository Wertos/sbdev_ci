<div style="min-height: 1.25em; color: rgb(17, 17, 17); font-family: Verdana, Arial, Helvetica, sans-serif; font-size: 14px;"><br />
    <strong><strong>Описание</strong></strong>:<br />
        Аддон использует собственный список заблокированных сайтов. Также, вы можете<br />
        составлять списки с собственными сайтами, которые вы хотите проксировать.<br />
        Аддон умеет определять, заблокирован ли у вас конкретный сайт, или нет, и<br />
        включаться только в том случае, если он заблокирован, однако доступна<br />
        опция &laquo;проксировать всегда&raquo;.<br />
        Можно использовать свой собственный прокси, а не прокси сервиса.<br />
        Заявлен режим &laquo;AntiDPI&raquo; для обхода провайдерского DPI, однако<br />
        у меня он не заработал.<br />
        Firefox-версия умеет проксировать все сайты, в отличие от Chrome-версии.<br />
<br />
    <strong><strong>Плюсы</strong></strong>:<br />
        Использует список заблокированных сайтов, обновляемый администраторами<br />
        Умеет определять, заблокирован ли сайт, и только в случае блокировки<br />
        использовать прокси.<br />
<br />
    <strong><strong>Минусы</strong></strong>:<br />
        В списке заблокированных сайтов небольшое количество адресов<br />
